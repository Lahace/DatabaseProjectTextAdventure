    public synchronized int intChoice(int min, int max)
    {
        inputReq = true;
        do
        {
            setFocusAnswer();
            try{wait();}catch (InterruptedException e){}
        }
        while(!isInt(getLastAnswer()) || Integer.parseInt(getLastAnswer())<min || Integer.parseInt(getLastAnswer())>max ||!isInt(getLastAnswer()));
        
        inputReq = false;
        return Integer.parseInt(getLastAnswer());
    }
    
    public synchronized String stringAnswer()
    {
        inputReq = true;
        do
        {
            setFocusAnswer();
            try{wait();}catch (InterruptedException e){}
        }
        while(getLastAnswer().equals(""));   
        inputReq = false;
        return getLastAnswer();
    }
    
