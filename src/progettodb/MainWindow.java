    public void setHP(int hp)
    {
        int diff;
        diff = Math.abs(hp-getHP());
        if(hp>getHP()) //CURA
        {
            jLabel14.setIcon(new javax.swing.ImageIcon(getClass().getResource("/progettodb/img/Green-Up-Arrow.svg.png")));
            jLabel14.setVisible(true);
            for(int i=Integer.parseInt(jLabel8.getText()); i<=hp && hp<=100; i++)
            {
               jLabel8.setText(Integer.toString(i));
               tsleep(1495/19-(14*diff)/19);
            }
            jLabel14.setVisible(false);
            jLabel14.setIcon(new javax.swing.ImageIcon(getClass().getResource("/progettodb/img/Green-Up-Arrow.svg.png")));
        }
        else if(hp<getHP()) //DANNO
        {
            jLabel14.setIcon(new javax.swing.ImageIcon(getClass().getResource("/progettodb/img/Red-Down-Arrow.svg.png")));
            jLabel14.setVisible(true);
            for(int i=Integer.parseInt(jLabel8.getText()); i>=hp && hp>=0; i--)
            {
               jLabel8.setText(Integer.toString(i));
               tsleep(1495/19-(14*diff)/19);
            }
            jLabel14.setVisible(false);
            jLabel14.setIcon(new javax.swing.ImageIcon(getClass().getResource("/progettodb/img/Red-Down-Arrow.svg.png")));
        }
        return;
    }
    
